	<h1>SM64 Personal Best trend analysis</h1>
	<p>I started this project to get an idea of what is the typical rate of improvement over time for the 2 main categories for SM64 speedrunning.</p>
	<p>I used this data to see if a learning strategy involving <a href="https://en.wikipedia.org/wiki/Spaced_repetition">Spaced Repetition</a> and <a href="https://en.wikipedia.org/wiki/Varied_practice">Interleaved Practice</a> could provide any benefits.</p>
	<p>My hope is that I will have a greater long term retention of the skills required to play the game well and that I would be able to pick up more advanced techniques faster than is expected.</p>
	<p>Data was harvested in early January 2022.</p>
